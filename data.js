/* 위 KDC_QUIZ 를 실제 문제 카드로 만들어주는 부분이에요.
   (선생님은 여기를 고칠 필요가 없어요) */
function kdcLabel(code) {
  const k = KDC.find(x => x.code === code);
  return `${k.code} ${k.name}`;
}

function shuffleCodes(list) {
  const a = list.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function kdcQuizSteps() {
  return KDC_QUIZ.map(q => {
    const codes = shuffleCodes([q.code, ...q.others]);   // 보기 순서 섞기
    const k = KDC.find(x => x.code === q.code);
    return {
      icon: '🔎',
      heading: q.heading,
      body: '몇 번대 서가에 가면 찾을 수 있을까요?',
      quiz: {
        choices: codes.map(kdcLabel),
        answer: codes.indexOf(q.code),                   // 정답 번호는 자동으로
        hint: q.hint,
        explain: `${k.code}번대 ${k.name} 책방에 있어요. (${k.ex})`
      }
    };
  });
}

